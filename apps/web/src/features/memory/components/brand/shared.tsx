export function BulletList({ text }: { text: string | null | undefined }) {
  if (!text) return null;
  const lines = text.split(/\r?\n/).map((l) => l.trim()).filter(Boolean);
  
  const content = (
    <ul className="space-y-2">
      {lines.map((line, i) => (
        <li key={i} className="flex gap-2 text-sm text-slate-700 font-medium leading-relaxed">
          <span className="mt-1 h-1.5 w-1.5 shrink-0 rounded-full bg-gradient-to-br from-pink-500 to-cyan-500" />
          <span>{line.replace(/^[-*•]\s*/, '')}</span>
        </li>
      ))}
    </ul>
  );

  if (lines.length > 4) {
    return (
      <ExpandableContent title="Listado detallado" icon="list">
        {content}
      </ExpandableContent>
    );
  }

  return content;
}

